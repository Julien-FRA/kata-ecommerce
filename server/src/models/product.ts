import ProductSchema from "../utils/product";

var mongoose = require("mongoose");

var schema: ProductSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    unique: true,
  },
  category: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  size: {
    type: String,
    required: true,
  },
  color: {
    type: String,
    required: true,
  },
});

var product = new mongoose.model("Product", schema);

module.exports = product;
